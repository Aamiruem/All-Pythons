# Assignment Operators => =, +=, -=, *=, /=, %=, //=, **=, &=, |=, ^=, >>=, <<=
# Assignment Operators are used to assign values to variables defined
# => greater than or equal to, less than or equal to, equal to, not equal to and less than`

num = 10
print("num = ", num)

num >= 5
print("Greater than or equal to = ", num)

num <= 5
print("Less than or equal to = ", num)

num += 5
print("num = num + 5 = ", num)

num -= 5
print("num = num - 5 = ", num)

num *= 5
print("num = num * 5 = ", num)


num /= 5
print("num = num / 5 = ", num)

num %= 5
print("num = num % 5 = ", num)

num //= 5
print("num = num // 5 = ", num)

num **= 5
print("num = num ** 5 = ", num)

num &= 5
print("num = num & 5 = ", num)

num |= 5
print("num = num | 5 = ", num)

num ^= 5
print("num = num ^ 5 = ", num)

num >>= 5
print("num = num >> 5 = ", num)

num <<= 5
print("num = num << 5 = ", num)

