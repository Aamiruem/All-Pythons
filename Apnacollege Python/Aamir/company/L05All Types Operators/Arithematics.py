# Arithmetic Operators => +, -, *, /, %, **, //
# Addition, Subtraction, Multiplication, Division, Modulo, Exponentiation, Floor Division

a = 5
b = 2

# Addition
c = a + b
print("This is the Addition of a and b = ", c)

# Subtraction
d = a - b
print("This is the Subtraction of a and b = ", d)


# Multiplication
e = a * b
print("This is the Multiplication of a and b = ", e)

# Division
f = a / b
print("This is the Division of a and b = ", f)

# Modulo
# what is the remainder of 5 divided by 2? The answer is 1 because 5 divided by 2 is 2 with a remainder of 1.
# Remaining Remainder is the answer
g = a % b
print("This is the Modulo of a and b = ", g)

# Exponentiation
h = a ** b
print("This is the Exponentiation of a and b = ",h)

# Floor Division
# solve the division problem by using floor division
# what is the floor division of 5 and 2? The answer is 2.5 because 5 divided by 2 is 2 with a remainder of 1.5 and 2.5 is the floor division of 5 and 2.
i = a // b
print("This is the Floor Division of a and b = ", i)


