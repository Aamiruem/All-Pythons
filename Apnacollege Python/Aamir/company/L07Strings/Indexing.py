# Indexing  =>  [start:stop:step]
# 1. start: stop: step
# 2. start: stop: step: fillvalue

str = "Aamir Hussain"
print(str[0:5])
print(str[4])
print(str[0:5:2])


# python indexing code example
a = "GeeksforGeeks"
print(a[0:6])
print(a[0:6:2])

print(a[-2:])
print(a[-2:0:-1])
print(a[::-1])

print(a[-1:-4])
print(a[1:4:2])

